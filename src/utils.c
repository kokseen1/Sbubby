static inline void *ptr_max(void *x, void *y)
{
    return x > y ? x : y;
}

// Equality comparison with low precision for timestamps
inline int dbl_eq(const double a, const double b)
{
    return fabs(a - b) < 0.01;
}

// Return 0 on success
int pop_char_at_idx(char *str, int idx)
{
    size_t len = strlen(str);

    // Out of range
    if (idx >= len || idx < 0)
        return 1;

    // Ovelap the string at idx with the rest of the string starting from idx+1
    memmove(&str[idx], &str[idx + 1], strlen(str) - idx);
    return 0;
}

// Returns pointer to start of next word relative to starting index
// Similar to Ctrl + RIGHT
char *get_next_word(char *str, int idx)
{
    size_t len = strlen(str);

    // Skip leading chars
    while (idx < len && !isspace(str[idx]))
        idx++;

    // Skip leading spaces
    while (idx < len && isspace(str[idx]))
        idx++;

    return &str[idx];
}

// Returns pointer to end of previous word relative to starting index
// Similar to Ctrl + LEFT
char *get_prev_word(char *str, int idx)
{
    // Skip trailing spaces
    while (idx >= 0 && isspace(str[idx]))
        idx--;

    // Find previous whitespace if any
    while (idx >= 0 && !isspace(str[idx]))
        idx--;

    return &str[idx + 1];
}

// Pop characters from a string starting from pointer
// Return 0 on success
int pop_range(char *start, size_t sz)
{
    size_t len = strlen(start);
    if (sz <= 0 || sz > len)
        return 1;
    memmove(start, start + sz, len - sz + 1);
    return 0;
}

// Pop the last char from a string
// Return 0 on success
int pop_char(char *str)
{
    size_t len = strlen(str);
    if (len <= 0)
        return 1;

    str[len - 1] = '\0';
    return 0;
}

void pop_word(char *str)
{
    size_t len = strlen(str);
    if (len <= 0)
        return;
    *get_prev_word(str, len - 1) = '\0';
}

// Convert a HH:MM:SS string to a timestamp in seconds
double str_to_timestamp(const char *str)
{
    double timestamp = 0.0;
    struct slre_cap caps[3];

    if (slre_match("^([0-9]+):([0-9]+):([0-9]+[,|\\.][0-9]+)$", str, strlen(str), caps, 3) > 0)
    {
        if (caps[0].len > 0 && caps[1].len > 0 && caps[2].len > 0)
        {
            char hh[16] = {0};
            char mm[16] = {0};
            char ss[16] = {0};

            strncpy(hh, caps[0].ptr, caps[0].len);
            strncpy(mm, caps[1].ptr, caps[1].len);
            strncpy(ss, caps[2].ptr, caps[2].len);

            char *pos = strchr(ss, ',');
            if (pos)
                *pos = '.';

            timestamp = strtol(hh, NULL, 10) * 3600 + strtol(mm, NULL, 10) * 60 + strtod(ss, NULL);
        }
    }

    return timestamp;
}

// Convert timestamp from seconds to HH:MM:SS format
void timetamp_to_str(const double ts, char *ts_str)
{
    const int h = ts / 3600;
    const int m = fmod((ts / 60), 60);
    const double s = fmod(ts, 60);
    sprintf(ts_str, "%02d:%02d:%06.3f", h, m, s);

    // Convert period to comma
    char *ret = strchr(ts_str, '.');
    if (ret)
        *ret = ',';
}
